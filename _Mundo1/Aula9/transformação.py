frase = 'Curso em Vídeo Python'
print(frase.replace('Python','Android'))#substitui uma string por outra
print(frase.upper())#coloca em maiusculas
print(frase.lower())#coloca em minusculas
print(frase.capitalize())#coloca tudo em minusculo e o primeiro fica maiusculo
print(frase.title())#coloca maiusculo palavra por palavra, reconhecendo pelo espaço
frase = '   Aprenda Python  '
print(frase.strip())#remove os espaços inuteis, que não tem sentido
print(frase.rstrip())#remove só os espaços da direita
print(frase.lstrip())#remove só os espaços da esquerda